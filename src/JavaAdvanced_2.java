public class JavaAdvanced_2 {
    public static void main(String[] args) {
        int a = 150;
        int b = 75;
        int plus = a + b;
        int multi = a * b;
        int division = a / b;
        int remainder = a % b;
        System.out.println("a + b = " + plus);
        System.out.println("a * b = " + multi);
        System.out.println("a / b = " + division);
        System.out.println("a % b = " + remainder);
        System.out.println(remainder == 0);
        System.out.println("Smile = \u263A");
    }
}
